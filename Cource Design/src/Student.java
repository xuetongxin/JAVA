class Student {
	private int id;
	private String name;
	private int age;
	private String grade;
	Student() {
	}
	void SetId(int id) {
		this.id=id;
	}
	int GetId() {
		return id;
	}
	void SetName(String name) {
		this.name=name;
	}
	String GetName() {
		return name;
	}
	void SetAge(int age) {
		this.age=age;
	}
	int GetAge() {
		return age;
	}
	void SetGread(String grade) {
		this.grade=grade;
	}
	String GetGrade() {
		return grade;
	}
}
