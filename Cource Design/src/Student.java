
class Student {
	 double id;
	 String name;
	 int age;
	 String grade;

	Student() {
	}

	void  SetId(double id) {
		this.id = id;
	}

	double GetId() {
		return id;
	}

	void SetName(String name) {
		this.name = name;
	}

	String GetName() {
		return name;
	}

	void SetAge(int age) {
		this.age = age;
	}

	int GetAge() {
		return age;
	}

	void SetGread(String grade) {
		this.grade = grade;
	}

	String GetGrade() {
		return grade;
	}

	
}
